    <h1>Mini stations d'épuration (MSE)</h1>
    <div>
        Une MSE est un système mis en place après votre fosse toute eaux (pré-traitement). Elle remplace le drainage filtrant (traitement) appelé épandage.<br>
        La mini station d'épuration ou micro station d'épuration est un traitement à l'oxygène qui est généré par une pompe raccordé électriquement. Cela vous permettra de récupérer une eau traitée et claire non potable qui pourra soit être réinjecté dans votre réseau principal complété d'une sur-filtration et un adoucisseur ou simplement utilisé pour l'arrosage de votre jardin.<br>

        Pour une autonomie quasi complète nous vous conseillons un collecteur d'eau en complément, nous nous occupons aussi du drainage, fossés et gouttière nécessaire.<br><br>
