    <h1>Fosses septiques</h1>
    <div>
        Une fosse septique est un système d'assainissement effectuant la collecte, le traitement, l'épuration et l'infiltration ou le rejet des eaux usées domestiques des habitations non raccordées au réseau d'assainissement collectif.<br>
        Il existe différents systèmes d'assainissement et surtout d'épandage. <br>
        Prestation / Réalisation :
        <ol>
            <li>Se renseigner auprès du SPANC*</li>
            <li>Établissement du devis</li>
            <li>Exécution des travaux</li>
            <li>Validation du SPANC*</li>
            <li>Finitions si demandé</li>
        </ol>
        <span class="small">
						 * Le SPANC (Service Public d'Assainissement Non Collectif) est l'organisme agréé par la commune de l'habitation qui est chargée du contrôle et des autorisations relatives à l'installation de tous les systèmes d'assainissement.<br>
						 Nous pouvons vous mettre en relation avec le SPANC qui définira l'installation et la faisabilité de votre projet. Ainsi nous pourrons établir le devis en accord avec leur recommandation. L'intervention du SPANC est obligatoire et à votre charge avant toute interventions de notre part, une fois les travaux commencés l'organisme intervient pour vérifier le respect du montage de l'ensemble de l'installation avant de passer aux finitions.</span>
        <br>
